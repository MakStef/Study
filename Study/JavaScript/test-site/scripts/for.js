/*
Цикл for в JS має такий вигляд:
    for (let <iм'я змінної> =  <значення>; <ім'я змінної> <оператор> <значення>; <ім'я>+<++/-->) {
        <код>
    }
*/

// Поки Var мешна за 10, виводиться значення Var
for (let Var = 0; Var < 10; Var++) {
    alert(Var)
}
// Поки Var більша за 10, виводиться значення Var
for (let Var = 20; Var > 10; Var--) {
    alert(Var)
}
 